package tw.slmt.collada;

public class Contributor {
	protected String author;
	protected String author_email;
	protected String author_website;
	protected String authoring_tool;
	protected String comments;
	protected String copyright;
	protected String source_data;
	
	public String getAuthor() {
		return author;
	}
	
	public String getAuthor_email() {
		return author_email;
	}
	
	public String getAuthor_website() {
		return author_website;
	}
	
	public String getAuthoring_tool() {
		return authoring_tool;
	}
	
	public String getComments() {
		return comments;
	}
	
	public String getCopyright() {
		return copyright;
	}
	
	public String getSource_data() {
		return source_data;
	}
	
}
